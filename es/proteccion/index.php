          <h2 class="titulos text-center col-12">POLÍTICA DE TRATAMIENTO PROTECCIÓN DE DATOS PERSONALES</h2> <br>
          <h2 class="titulos col-12 text-center">Eurochem</h2>
        </div>
        <div class="row mt-4">
          <p class="text_general">
            En cumplimiento a lo dispuesto en la Ley estatutaria 1581 de 2012 y
            a su Decreto Reglamentario 1377 de 2013, eurochem
            adopta la presente política para el tratamiento protección de datos
            personales, la cual será debidamente comunicada a todas las personas
            naturales titulares de datos que la entidad ha obtenido u obtenga en
            adelante, en el ejercicio de su actividad comercial.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 1</h2>
          <h2 class="titulos text-center col-12">MARCO LEGAL</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            La Política de Tratamiento de Datos Personales de eurochem,
            se fundamenta en el Artículo 15 de la Constitución Política, las leyes
             1266 de 2008 y 1581 de 2012 y los Decretos Reglamentarios 1727 de 2009,
             2952 de 2010 y 1377 de 2013 y demás normas que lleguen a adicionarlas o modificarlas.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 2</h2>
          <h2 class="titulos text-center col-12">DEFINICIONES</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            Para efectos de la presente política y de conformidad con la
            legislación aplicable, a menos que el contexto exija lo contrario,
            los términos que se indican a continuación tendrán el siguiente significado:
          </p>
          <p class="text_general">

            <strong>3.1. AUTORIZACIÓN:</strong> Es el permiso de carácter legal o
            contractual o el consentimiento previo, expreso e informado del titular
            para llevar a cabo el tratamiento de datos personales.
          </p>
          <p class="text_general">
            <strong>3.2. AVISO DE PRIVACIDAD:</strong> Es la comunicación verbal o
            escrita generada por el responsable dirigida al titular para el tratamiento
            de sus datos personales, mediante la cual se le informa acerca de la
            existencia de las políticas de tratamiento de información que le serán
            aplicables, la forma de acceder a las mismas y las finalidades del tratamiento
            que se pretende dar a los datos personales.
          </p>
          <p class="text_general">
            <strong>3.3. BASE DE DATOS:</strong> Es el conjunto organizado de datos
            personales que sea objeto de tratamiento.
          </p>
          <p class="text_general">
            <strong>3.4. CAUSAHABIENTE:</strong> Es la persona que ha sucedido a otra
            por causa del fallecimiento de ésta (heredero).
          </p>
          <p class="text_general">
            <strong>3.5. DATO PERSONAL: </strong> Es cualquier información vinculada o
            que pueda asociarse a una o varias personas naturales determinadas o determinables.
          </p>
          <p class="text_general">
            <strong>3.6. DATO PÚBLICO:</strong> Es el dato personal que de conformidad
            con la Constitución o la Ley sea calificado como tal y aquel que no sea
            semiprivado, privado o sensible. Son considerados datos públicos, entre otros:
            los datos relativos al estado civil de las personas, a su profesión u oficio
             y a su calidad de comerciante o de servidor público. Por su naturaleza, los
             datos públicos pueden estar contenidos, entre otros, en registros públicos,
              documentos públicos, gacetas y boletines oficiales y sentencias judiciales
               debidamente ejecutoriadas que no estén sometidas a reserva.
          </p>
          <p class="text_general">
            <strong>3.7. DATO PRIVADO:</strong> Es el dato que por su naturaleza íntima
            o reservada sólo es relevante para el titular.
          </p>
          <p class="text_general">
            <strong>3.8. DATO SENSIBLE:</strong> Es aquel dato que puede llegar a
            afecta la intimidad del titular o cuyo uso indebido puede generar su
            discriminación, tales como aquellos que revelen el origen racial o étnico,
            la orientación política, las convicciones religiosas o filosóficas, la
            pertenencia a sindicatos, organizaciones sociales, de derechos humanos o
            que promueva intereses de cualquier partido político o que garanticen los
            derechos y garantías de partidos políticos de oposición, así como los datos
            relativos a la salud, a la vida sexual, y los datos biométricos.
          </p>
          <p class="text_general">
            <strong>3.9. ENCARGADO DEL TRATAMIENTO:</strong> Es la persona natural o
            jurídica, pública o privada que por sí misma o en asocio con otros, realice
            el Tratamiento de datos personales por cuenta del Responsable del Tratamiento.
          </p>
          <p class="text_general">
            <strong>3.10. HABEAS DATA:</strong> Es el derecho fundamental de cualquier
            persona a conocer, actualizar y rectificar la información que sobre ella se
            haya en el banco de datos y en archivos de entidades públicas y privadas.
          </p>
          <p class="text_general">
            <strong>3.11. LEY DE PROTECCIÓN DE DATOS PERSONALES:</strong> Es la Ley 1581 de
            2012 y sus Decretos reglamentarios o las normas que los modifiquen, complementen
             o sustituyan.
          </p>
          <p class="text_general">
            <strong>3.12. RESPONSABLE DEL TRATAMIENTO:</strong> Es eurochem, que es la
            persona jurídica, de naturaleza privada que por sí misma o en asocio con otros,
            decide sobre la base de datos y/o Tratamiento de los datos.
          </p>
          <p class="text_general">
            <strong>3.13. TITULAR:</strong> Es la persona natural cuyos datos personales
            sean objeto de Tratamiento.
          </p>
          <p class="text_general">
            <strong>3.14. TRATAMIENTO:</strong> Es cualquier operación o conjunto de
            operaciones sobre datos personales, tales como la recolección, almacenamiento,
            uso, circulación o supresión.
          </p>
          <p class="text_general">
            <strong>3.15. TRANSFERENCIA:</strong> Es cuando el responsable y/o encargado
             del tratamiento de datos personales, ubicado en Venezuela, envía la información
             o los datos personales a un receptor, que a su vez es responsable del tratamiento
              y se encuentra dentro o fuera del país.
          </p>
          <p class="text_general">
            <strong>3.16. TRANSMISIÓN:</strong> tratamiento de datos personales que
            implica la comunicación de los mismos dentro o fuera del territorio de la
            República Bolivariana de Venezuela cuando tenga por objeto la realización de un
            tratamiento por el encargado por cuenta del responsable.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 3</h2>
          <h2 class="titulos text-center col-12">PRINCIPIOS RECTORES PARA EL TRATAMIENTO DE DATOS PERSONALES</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            Al tratamiento de datos personales en eurochem, en desarrollo,
            interpretación y aplicación de la ley 1581 de 2012 por la cual se dictan
            disposiciones generales para la protección de datos personales y las normas
            que la complementan, modifican o adicionan, se aplicarán de manera armónica
            e integral, los siguientes principios rectores:
          </p>
          <p class="text_general">
            <strong>4.1. LEGALIDAD:</strong> El tratamiento de los datos personales es una
            actividad reglada que debe sujetarse a lo estableció en la ley.
          </p>
          <p class="text_general">
            <strong>4.2. FINALIDAD:</strong> El tratamiento que se haga de los datos
            personales recogidos debe obedecer a una finalidad legítima de acuerdo con la
            Constitución y la Ley, la cual debe ser informada al titular.
          </p>
          <p class="text_general">
            <strong>4.3. LIBERTAD:</strong> El tratamiento de datos personales solo puede
             ejercerse con el consentimiento previo, expreso, e informado del titular o
             por autorización legal. Los datos personales no podrán ser obtenidos o
             divulgados sin previa autorización, o en ausencia de mandato legal o
             judicial que releve el consentimiento.
          </p>
          <p class="text_general">
            <strong>4.4. VERACIDAD O CALIDAD:</strong> La información sujeta a tratamiento
             debe ser veraz, completa, exacta, actualizada, comprobable y comprensible.
             No se realizará tratamiento de datos parciales, incompletos, fraccionados o
              que induzcan a error.
          </p>
          <p class="text_general">
            <strong>4.5. TRANSPARENCIA:</strong> En el tratamiento de datos se garantiza
            el derecho del titular a obtener del responsable del tratamiento o del
            encargado del tratamiento, en cualquier momento y sin restricciones,
            información acerca de la existencia de datos que le conciernan.
          </p>
          <p class="text_general">
            <strong>4.6. ACCESO Y CIRCULACIÓN RESTRINGIDA:</strong> El tratamiento de
             los datos personales se sujeta a los límites que se derivan de su naturaleza,
              de las disposiciones de la ley y la Constitución. En este sentido, el
              tratamiento sólo podrá hacerse por personas autorizadas por el titular
              y/o por las personas previstas en la ley. Los datos personales, salvo
              la información pública, no podrán estar disponibles en internet u otros
               medios de divulgación o comunicación masiva, salvo que el acceso sea
               técnicamente controlable para brindar un conocimiento restringido sólo
                a los titulares o terceros autorizados conforme a la ley.
          </p>
          <p class="text_general">
            <strong>4.7. SEGURIDAD:</strong> La información sujeta a tratamiento por
             parte del Responsable, se deberá proteger mediante el uso de medidas técnicas,
              humanas y administrativas que sean necesarias para otorgar seguridad a los
              registros evitando su adulteración, pérdida, consulta, uso o acceso no
               autorizado o fraudulento.
          </p>
          <p class="text_general">
            <strong>4.8. CONFIDENCIALIDAD:</strong> El Responsable está obligado a
            garantizar la reserva de la información, inclusive después de finalizada
             la relación que dio origen a las labores que comprende el tratamiento,
             pudiendo sólo realizar suministro o comunicación de datos personales
             cuando ello corresponda al desarrollo de las actividades autorizadas en la ley.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 4</h2>
          <h2 class="titulos text-center col-12">DERECHOS QUE LE ASISTEN AL TITULAR DE DATOS PERSONALES OBJETO DE TRATAMIENTO.</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            Los titulares de datos personales, que sean objeto de tratamiento por parte
            de eurochem, directamente o por medio de un representante y/o
             apoderado o sus causahabientes, podrán ejercer los siguientes derechos:
             <br>
            5.1. Conocer, actualizar y rectificar sus datos personales frente a eurochem
             en su condición de responsable del tratamiento. Este derecho se podrá ejercer,
             entre otros, frente a datos parciales, inexactos, incompletos, fraccionados, que
              induzcan a error, o aquellos cuyo tratamiento esté expresamente prohibido o no
              haya sido autorizado.
            <br>
            5.2. Solicitar prueba de la autorización otorgada para el tratamiento de datos
            personales, salvo cuando expresamente se exceptúe como requisito para el tratamiento
            (casos en los cuales no es necesaria la autorización).
            <br>
            5.3. Ser informado respecto del uso de los datos personales.
            <br>
            5.4. Presentar ante la Superintendencia de Industria y Comercio quejas por
            infracciones a lo dispuesto en la Ley 1581 de 2012 y las demás normas que la
             modifiquen, adicionen o complementen.
            <br>
            5.5. Revocar la autorización y/o solicitar la supresión del dato cuando en
            el Tratamiento no se respeten los principios, derechos y garantías constitucionales
            y legales.
            <br>
            5.6. Acceder en forma gratuita a sus datos personales que hayan sido objeto de tratamiento.
          </p>
          <p class="text_general">
          </p>
          <p class="text_general">
            <strong>PARÁGRAFO PRIMERO:</strong> Los derechos de los titulares establecidos
            en la presente política y en la Ley podrán ejercerse por las siguientes personas:
            <br>
            a). Por el titular, quien deberá acreditar su identidad en forma.
            <br>
            b). Por los causahabientes del titular, quienes deberán acreditar tal calidad.
            <br>
            c). Por el representante y/o apoderado del titular, previa acreditación de la
            representación o apoderamiento.
            <br>
            d). Por estipulación a favor de otro o para otro.
            <br>
            e). Los derechos de los niños, niñas y adolescentes se ejercerán por las personas
            que estén facultadas para representarlos.
          </p>
          <p class="text_general">
            <strong>PARÁGRAFO SEGUNDO:</strong> En el Tratamiento se asegurará el respeto a
             los derechos prevalentes de los niños y adolescentes.
             <br>
            Queda proscrito el Tratamiento de datos personales de niños y adolescentes,
            salvo aquellos datos que sean de naturaleza pública o respecto de los cuales se
             haya obtenido autorización expresa de sus representantes, previa información
             sobre el tratamiento.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 5</h2>
          <h2 class="titulos text-center col-12">DEBERES DE eurochem FRENTE AL TRATAMIENTO DE DATOS PERSONALES.</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            Sin perjuicio de las disposiciones previstas en la ley, en virtud de la
            presente política de tratamiento y protección de datos personales son deberes
            de eurochem, tanto en su calidad de responsable como de Encargado de
            tratamiento, si es del caso, los siguientes:
          </p>
          <p class="text_general">
            6.1. Solicitar y conservar, en las condiciones previstas en esta política,
            copia de la autorización otorgada por el titular para el tratamiento de los
             datos personales.
             <br>
            6.2. Informar de manera clara y suficiente al titular sobre la finalidad de
             la recolección y los derechos que le asisten por virtud de la autorización otorgada.
             <br>
            6.3. Informar a solicitud del titular sobre el uso dado a sus datos personales.
            <br>
            6.4. Tramitar las consultas y reclamos formulados en los términos señalados en
             la presente política y en la Ley.
             <br>
            6.5. Velar por que la información sea veraz, completa, exacta, actualizada,
            comprobable y comprensible.
            <br>
            6.6. Mantener la confidencialidad de la información en los términos establecidos
             en la presente política y la Ley.
             <br>
            6.7. Conservar la información bajo las condiciones de seguridad necesarias para
            impedir su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.
            <br>
            6.8. Actualizar la información cuando sea necesario. En los eventos en que actúe
             como encargado de tratamiento dicha actualización deberá realizarla dentro de
             los cinco (5) días hábiles contados a partir de recibir la solicitud.
             <br>
            6.9. Rectificar los datos personales cuando ello sea procedente.
            <br>
            6.10. Establecer que el Responsable del tratamiento esté autorizado para
            suministrar los datos personales que llegaré a tratar como Encargado.
            <br>
            6.11. Realizar oportunamente la actualización, rectificación o supresión de los datos.
            <br>
            6.12. En los eventos en que actúe como encargado de datos deberá registrar
            en la base de datos respectiva la leyenda “reclamo en trámite” en la forma
            en que se establece en la presente política y en la ley.
            <br>
            6.13. En los eventos en que actúe como encargado de datos deberá insertar
            en la base de datos la leyenda “información en discusión judicial” una vez
            notificado por parte de la autoridad competente sobre procesos judiciales
            relacionados con la calidad del dato personal.
            <br>
            6.14. Abstenerse de circular información que esté siendo controvertida por
             el titular y cuyo bloqueo haya sido ordenado por la Superintendencia de
             Industria y Comercio.
             <br>
            6.15. Permitir el acceso a la información únicamente a las personas autorizadas
            por el titular o facultadas por la ley para dicho efecto.
            <br>
            6.16. Informar a la Superintendencia de Industria y Comercio cuando
            se presenten violaciones a los códigos de seguridad y existan riesgos
            en la administración de la información de los titulares.
            <br>
            6.17. Cumplir las instrucciones y requerimientos que imparta la Superintendencia
            de Industria y Comercio.
            <br>
            6.18. Suministrar a los encargados del tratamiento únicamente los datos personales
             cuyo tratamiento esté previamente autorizado. Para efectos de la transmisión
             nacional o internacional de los datos se deberá suscribir un contrato de transmisión
              de datos personales o pactar cláusulas contractuales según lo establecido en
               el artículo 25 del decreto 1377 de 2013.
               <br>
            6.19. Garantizar que la información que se suministre a los Encargado de
             tratamiento sea veraz, completa, exacta, actualizada, comprobable y comprensible.
             <br>
            6.20. Comunicar de forma oportuna a los encargados de tratamiento todas
            las novedades respecto de los datos que previamente le haya suministrado
            y adoptar las demás medidas necesarias para que la información suministrada
             a este se mantenga actualizada.
             <br>
            6.21. Informar de manera oportuna a los encargados de tratamiento las
            rectificaciones realizadas sobre los datos personales para que éste proceda
            a realizar los ajustes pertinentes.
            <br>
            6.22. Exigir a los encargados de tratamiento, en todo momento, el respeto
            a las condiciones de seguridad y privacidad de la información del titular.
            <br>
            6.23. Informar a los encargados de tratamiento cuando determinada información
            se encuentre en discusión por parte del titular, una vez se haya presentado la
             reclamación y no haya finalizado el trámite respectivo.
             <br>
            6.24. Cumplir las instrucciones y requerimientos que imparta la Superintendencia
            de Industria y Comercio.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 6</h2>
          <h2 class="titulos text-center col-12">AUTORIZACIONES Y CONSENTIMIENTO DEL TITULAR.</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            Sin perjuicio de las excepciones previstas en la Ley, en el tratamiento
            de datos personales del titular se requiere la autorización previa e
            informada de éste, la cual deberá ser obtenida por cualquier medio que
             pueda ser objeto de consulta posterior.
          </p>
          <p class="text_general">
            <strong>PARÁGRAFO:</strong> La autorización del titular de la información
             no será necesaria en los siguientes casos:
          </p>
          <p class="text_general">
            a). Cuando la información sea requerida por una entidad pública o
            administrativa en ejercicio de sus funciones legales o por orden judicial.
            <br>
            b). Cuando los datos sean de naturaleza pública.
            <br>
            c). Cuando se presente un caso de urgencia médica o sanitaria.
            <br>
            d). Cuando el tratamiento de información se encuentre autorizado por la ley.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 7</h2>
          <h2 class="titulos text-center col-12">TRATAMIENTO AL CUAL SERÁN SOMETIDOS LOS DATOS Y FINALIDAD DEL MISMO.</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            eurochem podrá hacer uso de los datos personales, previa autorización
            legal o expresa del titular para las siguientes finalidades:
          </p>
          <p class="text_general">
            8.1. Ejecución de las relaciones contractuales existentes con sus accionistas,
            administradores, trabajadores, clientes, proveedores, incluido el pago de
            obligaciones.
            <br>
            8.2. Prestación de los servicios y/o distribución de los productos requeridos
            por los clientes.
            <br>
            8.3. Comunicación de información comercial, publicitaria, promocional, sobre
            productos y servicios.
            <br>
            8.4. Evaluación de la calidad del servicio.
            <br>
            8.5. Realización de estudios de mercadeo, big data y marketing directo.
            <br>
            8.6. Desarrollo del proceso de sección, evaluación y vinculación del personal.
            <br>
            8.7. Soporte de los procesos de auditoria.
            <br>
            8.8. Suministro, envío y entrega de los datos personales a sus filiales,
            vinculadas o subordinaras o a terceros encargados de tratamiento de datos,
            ubicados en Venezuela o en el exterior, cuando sea necesario para el tratamiento
            y siempre que el tercero cuente con políticas adecuadas de protección de datos
            de conformidad con la ley y se cuente con la debida autorización por parte del
            titular.
            <br>
            8.9. Vigilancia para garantizar la seguridad de las personas, los bienes y
            las instalaciones de la empresa.
          </p>
        </div>
        <div class="row justify-content-center mt-3">
          <h2 class="titulos text-center col-12">CAPÍTULO 8</h2>
          <h2 class="titulos text-center col-12">TRANSFERENCIA Y TRANSMISIÓN INTERNACIONAL DE DATOS PERSONALES.</h2>
        </div>
        <div class="row mt-1">
          <p class="text_general">
            eurochem en cumplimiento del desarrollo de su objeto comercial
            podrá efectuar transferencia y transmisión de datos personales de los titulares.
          </p>
          <p class="text_general">
            Para la transferencia internacionales de datos personales de los titulares,
            eurochem tomará las medidas necesarias para que los terceros
            conozcan y se comprometan a observar esta Política, bajo el entendió
            que la información personal que reciban, únicamente podrán ser utilizada
            para asuntos directamente relacionados eurochem y no podrá ser
            usada o destinada para propósito o fin diferente. Para la transferencia
            internacional de datos personales se observará lo previsto en el artículo
            26 de la Ley 1581 de 2012.
          </p>
          <p class="text_general">
            Las transmisiones internacionales de datos personales que efectúe
            eurochem, no requerirán ser informadas al Titular ni contar con
